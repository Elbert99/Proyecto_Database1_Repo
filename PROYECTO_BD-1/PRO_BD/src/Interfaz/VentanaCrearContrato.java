    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
		int i = cmboxContrato.getSelectedIndex();
		if(i==0){
			ErrorDeSelecion.setVisible(true);
		}
		else if(i==1){
			VentanaContHotelero ch = new VentanaContHotelero();
			ch.setVisible(true);
			VentanaCrearContrato.this.dispose();
		}
		else if(i==2){
			VentanaContTrans ct = new VentanaContTrans();
			ct.setVisible(true);
			VentanaCrearContrato.this.dispose();
		}
    }//GEN-LAST:event_btnCrearActionPerformed

